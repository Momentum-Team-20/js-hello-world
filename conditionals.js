let dogsAreCool = true
let catsAreMean = false

if (dogsAreCool) {
  console.log('Dogs are in fact very cool.')
} else {
  console.log('Dogs are not cool.')
}

if (catsAreMean) {
  console.log('Told ya, cats are mean.')
} else {
  console.log('Cats are lovely creatures of fluff.')
}

